  CREATE OR REPLACE PROCEDURE "CESFAMDATA"."SP_LISTMAKERBYNAME" (makerName IN VARCHAR2, fab_cur OUT SYS_REFCURSO)
AS
BEGIN
OPEN fab_cur
FOR SELECT fab.ID_FABRICANTE, fab.NOMBRE_FABRICANTE, fab.DESCRIPCION, fab.ESTADO_FABRICANTE
